    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
 String Username = this.userName.getText();
        String Email = this.Email.getText();
        String Password = this.Password.getText();
        String Status = "STAFF";

        try {
            if (Username.trim().isEmpty() || Email.trim().isEmpty() || Password.trim().isEmpty()) {
                JOptionPane.showMessageDialog(new JFrame(), "Please fill all the fields", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/bicycle_system", "root", "");
            insert = con1.prepareStatement("INSERT INTO users(name, email, password, status) VALUES (?, ?, ?, ?)");
            insert.setString(1, Username);
            insert.setString(2, Email);
            insert.setString(3, Password);
            insert.setString(4, Status);

            insert.executeUpdate();
            JOptionPane.showMessageDialog(this, "User added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            reset();
            table_update();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ManagStaffAccount.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (insert != null) insert.close();
                if (con1 != null) con1.close();
            } catch (SQLException ex) {
                Logger.getLogger(ManagStaffAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_AddBtnActionPerformed

