    private void table_update() {
        int CC;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/bicycle_system", "root", "");
            insert = con1.prepareStatement("SELECT * FROM users WHERE STATUS = 'STAFF'");
            Rs = insert.executeQuery();

            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable3.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                v2.add(Rs.getString("id")); // Add the id field
                v2.add(Rs.getString("name"));
                v2.add(Rs.getString("email"));
                DFT.addRow(v2);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                if (Rs != null) Rs.close();
                if (insert != null) insert.close();
                if (con1 != null) con1.close();
            } catch (SQLException ex) {
                Logger.getLogger(ManagStaffAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

